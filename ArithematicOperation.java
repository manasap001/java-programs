
public class ArithematicOperation {
	int addition(int a, int b)
	{
		int sum=a+b;
		System.out.println("in this method it adds two numbers");
		return sum;
	}
	float subtraction(float a, float b)
	{
		float sub=a-b;
		System.out.println("in this method it subtract two numbers");
		return sub;
	}
	double multiplication(double a, double b)
	{
		double mul=a*b;
		System.out.println("in this method it multiplies two numbers");
		return mul;
	}
	int division(int a, int b)
	{
		int mod=a/b;
		System.out.println("in this method it divides two numbers");
		return mod;
	}
	int modulus(int a, int b)
	{
		int mod=a+b;
		System.out.println("in this method it finds the mod value of two numbers");
		return mod;
	}
}
